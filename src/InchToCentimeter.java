public class InchToCentimeter {
    public static void main(String[] args) {
        final double centimeter = 2.54;
        double inch = 10.00;

        double inchToCentimeter = inch * centimeter;
        System.out.printf(" %.2f polegada(s) é igual a %.2f centímetros", inch, inchToCentimeter);
    }
}
